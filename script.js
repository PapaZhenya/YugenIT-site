function normalizeSearchText(value) {
  return (value || "")
    .toString()
    .toLowerCase()
    .replace(/\s+/g, " ")
    .trim();
}

function htmlToSearchText(html) {
  const box = document.createElement("div");
  box.innerHTML = html || "";
  return box.textContent || "";
}

function getTopicSearchText(topic) {
  const lessonText = lessons[topic]?.text || "";
  const topicContent =
    topic === "domain" ? cloudBasicsContent :
    topic === "docker" ? dockerContent :
    topic === "linux" ? linuxContent :
    topic === "devops" ? devopsContent :
    topic === "networking" ? networkingContent :
    topic === "server" ? windowsServerContent :
    topic === "security" ? securityContent :
    topic === "mikrotik" ? mikrotikBasicsContent :
    "";

  return `${lessonText} ${htmlToSearchText(topicContent)}`;
}

function toggleSearchTarget(element, isMatch) {
  element.classList.toggle("search-hidden", !isMatch);
  element.classList.toggle("search-match", isMatch);
}

function applySearchFilter() {
  if (!globalSearchInput || !searchStatus) return;

  const query = normalizeSearchText(globalSearchInput.value);
  const activePage = document.querySelector(".page.active");
  const allTargets = document.querySelectorAll(".card, .course, .lesson-section");

  if (!query) {
    allTargets.forEach(element => {
      element.classList.remove("search-hidden", "search-match");
    });
    searchStatus.textContent = "";
    return;
  }

  let checked = 0;
  let matched = 0;
  const activeTargets = activePage
    ? activePage.querySelectorAll(".card, .course, .lesson-section")
    : [];

  allTargets.forEach(element => {
    if (!activePage || !activePage.contains(element)) {
      element.classList.remove("search-hidden", "search-match");
      return;
    }

    const topic = element.dataset.topic;
    const searchableText = normalizeSearchText(`${element.textContent} ${getTopicSearchText(topic)}`);
    const isMatch = searchableText.includes(query);

    checked += 1;
    if (isMatch) matched += 1;
    toggleSearchTarget(element, isMatch);
  });

  if (!activeTargets.length) {
    searchStatus.textContent = "Search is ready for visible learning content.";
    return;
  }

  searchStatus.textContent = matched
    ? `Found: ${matched} / ${checked}`
    : "Nothing found in the visible content.";
}

