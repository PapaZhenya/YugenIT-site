function getXpForNextLevel(level) {
  const fixedThresholds = {
    1: 20,
    2: 50,
    3: 100,
    4: 200,
    5: 350,
    6: 500,
    7: 700,
    8: 900,
    9: 1200,
    10: 1500,
    11: 1800,
    12: 2200,
    13: 2600,
    14: 3000
  };

  if (fixedThresholds[level]) return fixedThresholds[level];
  if (level >= 15 && level < 20) return 500;
  if (level >= 20 && level < 30) return 800;
  if (level >= 30 && level < 40) return 1200;
  if (level >= 40 && level < 50) return 2000;
  return 2000;
}

function calculateLevelProgress(totalXp) {
  let level = 1;
  let xpIntoLevel = Math.max(0, Math.floor(Number(totalXp) || 0));

  while (level < 50) {
    const xpNeeded = getXpForNextLevel(level);

    if (xpIntoLevel < xpNeeded) {
      return {
        level,
        xpIntoLevel,
        xpNeeded,
        percent: Math.round((xpIntoLevel / xpNeeded) * 100)
      };
    }

    xpIntoLevel -= xpNeeded;
    level++;
  }

  return {
    level,
    xpIntoLevel,
    xpNeeded: getXpForNextLevel(level),
    percent: 100
  };
}

